/// Calculate Monte-Carlo 99th percentile maximum drawdown
/// Performs 5000 bootstrap simulations and returns the 99th percentile max drawdown
pub fn monte_carlo_dd_99(returns: &[f64], seed: u64) -> f64 {
    if returns.is_empty() {
        return 0.0;
    }

    const RUNS: usize = 5000;
    let mut max_drawdowns = Vec::with_capacity(RUNS);
    let mut rng = SimpleRng::new(seed);

    for _ in 0..RUNS {
        let mut equity: f64 = 1.0;
        let mut peak: f64 = 1.0;
        let mut max_dd: f64 = 0.0;

        for _ in 0..returns.len() {
            // Bootstrap sampling with replacement
            let idx = (rng.next() as usize) % returns.len();
            let r = returns[idx];
            equity *= 1.0 + r;
            peak = peak.max(equity);
            max_dd = max_dd.max(peak - equity);
        }

        max_drawdowns.push(max_dd);
    }

    max_drawdowns.sort_by(|a, b| a.partial_cmp(b).unwrap_or(std::cmp::Ordering::Equal));
    let idx = ((max_drawdowns.len() as f64) * 0.99) as usize;
    max_drawdowns.get(idx).copied().unwrap_or(0.0)
}

/// Calculate Walk-Forward Efficiency (WFE)
/// WFE = IS_sharpe / OS_sharpe
/// Must be > 0.5 for robust strategy
pub fn calculate_wfe(in_sample_returns: &[f64], out_sample_returns: &[f64]) -> f64 {
    if in_sample_returns.is_empty() || out_sample_returns.is_empty() {
        return 0.0;
    }

    fn calculate_sharpe(returns: &[f64]) -> f64 {
        if returns.is_empty() {
            return 0.0;
        }
        let mean = returns.iter().sum::<f64>() / returns.len() as f64;
        let variance =
            returns.iter().map(|&r| (r - mean).powi(2)).sum::<f64>() / returns.len() as f64;
        let std_dev = variance.sqrt();
        if std_dev == 0.0 {
            return 0.0;
        }
        mean / std_dev
    }

    let is_sharpe = calculate_sharpe(in_sample_returns);
    let os_sharpe = calculate_sharpe(out_sample_returns);

    if is_sharpe > 0.0 {
        (os_sharpe / is_sharpe).max(0.0)
    } else {
        0.0
    }
}

/// Calculate Profit Factor
/// Profit Factor = gross_profit / gross_loss
/// Must be > 1.5 for robust strategy
pub fn calculate_pf(trades: &[Trade]) -> f64 {
    if trades.is_empty() {
        return 0.0;
    }

    let mut gross_profit: f64 = 0.0;
    let mut gross_loss: f64 = 0.0;

    for trade in trades {
        let pnl = (trade.exit_price - trade.entry_price) * trade.quantity;
        if pnl > 0.0 {
            gross_profit += pnl;
        } else {
            gross_loss += pnl.abs();
        }
    }

    if gross_loss == 0.0 {
        if gross_profit > 0.0 {
            return f64::INFINITY;
        }
        return 0.0;
    }

    gross_profit / gross_loss
}

/// Calculate suggested stop loss levels based on ATR
fn calculate_suggested_stops(
    timeframes: &[TimeframeData],
    config: &PhaseConfluenceConfig,
) -> Vec<f64> {
    let mut stops = Vec::new();

    for tf in timeframes {
        if let Some(last) = tf.ohlcv.last() {
            let atr_period = 14;
            let closes: Vec<f64> = tf.ohlcv.iter().map(|d| d.close).collect();
            let highs: Vec<f64> = tf.ohlcv.iter().map(|d| d.high).collect();
            let lows: Vec<f64> = tf.ohlcv.iter().map(|d| d.low).collect();

            if let Ok(atr) = ta_engine::calculate_atr(&highs, &lows, &closes, atr_period) {
                if let Some(atr_value) = atr.last() {
                    // 2x ATR stop
                    stops.push(last.close - atr_value * 2.0);
                }
            }
        }
    }

    stops
}

/// Main entry point for processing commands with multi-timeframe data
/// Parses JSON input, calls super_signal::calculate_super_signal(), and returns JSON output
pub fn process_command(input_json: &str) -> String {
    // Parse JSON input
    let input: ProcessCommandInput = match serde_json::from_str(input_json) {
        Ok(i) => i,
        Err(e) => {
            return format!("{{\"error\":\"Failed to parse input: {}\"}}", e);
        }
    };

    // Use default config if not provided
    let config = input.config.unwrap_or_default();

    // Calculate super signal
    let super_signal = calculate_super_signal(&input.timeframes, &config);

    // Calculate suggested stops
    let suggested_stops = calculate_suggested_stops(&input.timeframes, &config);

    // Calculate robustness metrics if trades are provided
    let robustness_metrics = if let Some(ref trades) = input.trades {
        let returns: Vec<f64> = trades
            .iter()
            .map(|t| (t.exit_price - t.entry_price) / t.entry_price)
            .collect();

        // Split returns for WFE calculation (70% in-sample, 30% out-of-sample)
        let split_idx = (returns.len() as f64 * 0.7) as usize;
        let (is_returns, os_returns) = returns.split_at(split_idx);

        let mc_dd = monte_carlo_dd_99(&returns, 42);
        let wfe = calculate_wfe(is_returns, os_returns);
        let pf = calculate_pf(trades);

        // Check if all gates pass
        let gates_passed = mc_dd < 0.20 && wfe > 0.5 && pf > 1.5;

        RobustnessMetrics {
            monte_carlo_dd_99: mc_dd,
            walk_forward_efficiency: wfe,
            profit_factor: pf,
            gates_passed,
        }
    } else {
        RobustnessMetrics::default()
    };

    // Build output
    let output = ProcessCommandOutput {
        super_signal_score: super_signal.score,
        confidence: super_signal.confidence,
        inflection_points: super_signal.inflection_points,
        suggested_stops,
        robustness_metrics,
        phase_alignment: super_signal.phase_alignment,
        dominant_cycles: super_signal.dominant_cycles,
    };

    // Return JSON output
    match serde_json::to_string(&output) {
        Ok(json) => json,
        Err(e) => format!("{{\"error\":\"Failed to serialize output: {}\"}}", e),
    }
}

/// WASM-exposed function to run backtest with robustness gates
#[wasm_bindgen]
pub fn run_backtest_wasm(data_json: &str) -> String {
    let data: Vec<MarketData> = match serde_json::from_str(data_json) {
        Ok(d) => d,
        Err(e) => return format!("{{\"error\":\"{}\"}}", e),
    };

    let config = PhaseConfluenceConfig::default();
    let mut strategy = PhaseConfluenceStrategy::new(config);
    let _signals = strategy.generate_signals(&data);
    let result = strategy.calculate_backtest(10000.0);

    // Calculate robustness metrics
    let returns: Vec<f64> = result
        .trades
        .iter()
        .map(|t| (t.exit_price - t.entry_price) / t.entry_price)
        .collect();

    let robustness = if !returns.is_empty() {
        let split_idx = (returns.len() as f64 * 0.7) as usize;
        let (is_returns, os_returns) = returns.split_at(split_idx);

        let mc_dd = monte_carlo_dd_99(&returns, 42);
        let wfe = calculate_wfe(is_returns, os_returns);
        let pf = calculate_pf(&result.trades);
        let gates_passed = mc_dd < 0.20 && wfe > 0.5 && pf > 1.5;

        serde_json::json!({
            "monte_carlo_dd_99": mc_dd,
            "walk_forward_efficiency": wfe,
            "profit_factor": pf,
            "gates_passed": gates_passed
        })
    } else {
        serde_json::json!({
            "monte_carlo_dd_99": 0.0,
            "walk_forward_efficiency": 0.0,
            "profit_factor": 0.0,
            "gates_passed": false
        })
    };

    let output = serde_json::json!({
        "sharpe_ratio": result.sharpe_ratio,
        "max_drawdown": result.max_drawdown,
        "total_return": result.total_return,
        "trades": result.trades,
        "robustness": robustness
    });

    match serde_json::to_string(&output) {
        Ok(json) => json,
        Err(e) => format!("{{\"error\":\"{}\"}}", e),
    }
}

/// WASM-exposed function to generate signals in super-signal format
#[wasm_bindgen]
pub fn generate_signals_wasm(data_json: &str) -> String {
    // Try to parse as multi-timeframe data first (for super-signal format)
    if let Ok(_input) = serde_json::from_str::<ProcessCommandInput>(data_json) {
        return process_command(data_json);
    }

    // Fallback to single-timeframe format
    let data: Vec<MarketData> = match serde_json::from_str(data_json) {
        Ok(d) => d,
        Err(e) => return format!("{{\"error\":\"{}\"}}", e),
    };

    let config = PhaseConfluenceConfig::default();
    let mut strategy = PhaseConfluenceStrategy::new(config);
    let signals = strategy.generate_signals(&data);

    // Convert to super-signal format
    let output = serde_json::json!({
        "super_signal_score": 0.0,
        "confidence": 0.0,
        "inflection_points": [],
        "suggested_stops": [],
        "signals": signals,
        "robustness_metrics": RobustnessMetrics::default(),
        "phase_alignment": 1.0,
        "dominant_cycles": []
    });

    match serde_json::to_string(&output) {
        Ok(json) => json,
        Err(e) => format!("{{\"error\":\"{}\"}}", e),
    }
}

